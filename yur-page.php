			var fields = document.querySelector('.yur-calc-fields');
			var closeElements = document.querySelectorAll('[data-yur-calc-close]');

			if (!panel || !overlay || !title || !fields) {
				return;
			}

			var typeToCategories = {
				type1: [
					{ categoryKey: 'cathegorie1', questionKey: 'question1' },
					{ categoryKey: 'cathegorie2', questionKey: 'question2' }
				],
				type2: [
					{ categoryKey: 'cathegorie3', questionKey: 'question3' },
					{ categoryKey: 'cathegorie4', questionKey: 'question4' }
				],
				type3: [
					{ categoryKey: 'cathegorie5', questionKey: 'question5' },
					{ categoryKey: 'cathegorie6', questionKey: 'question6' }
				]
			};

			function createFieldset(titleText, name, options, onChange) {
				var fieldset = document.createElement('fieldset');
				fieldset.className = 'yur-calc-field yur-calc-field--wide yur-calc-choice';

				var legend = document.createElement('legend');
				legend.textContent = titleText;
				fieldset.appendChild(legend);

				options.forEach(function (option, index) {
					var label = document.createElement('label');
					label.className = 'yur-calc-choice__option';

					var input = document.createElement('input');
					input.type = 'radio';
					input.name = name;
					input.value = option.value;
					input.checked = index === 0;
					input.addEventListener('change', function () {
						if (input.checked) {
							onChange(option.value);
						}
					});

					var text = document.createElement('span');
					text.textContent = option.label;

					label.appendChild(input);
					label.appendChild(text);
					fieldset.appendChild(label);
				});

				return fieldset;
			}

			function createNumberInput(questionText) {
				var label = document.createElement('label');
				label.className = 'yur-calc-field yur-calc-field--wide';

				var span = document.createElement('span');
				span.textContent = questionText;

				var input = document.createElement('input');
				input.type = 'number';
				input.min = '0';
				input.step = '0.01';
				input.placeholder = 'Введите значение';

				label.appendChild(span);
				label.appendChild(input);

				return label;
			}

			function getFilledOptions(product, keys) {
				return keys
					.filter(function (key) {
						return product[key];
					})
					.map(function (key) {
						return {
							value: key,
							label: product[key]
						};
					});
			}

			function renderQuestionInput(product, questionKey) {
				var previousInput = fields.querySelector('[data-calc-question-input]');

				if (previousInput) {
					previousInput.remove();
				}

				if (!product[questionKey]) {
					return;
				}

				var input = createNumberInput(product[questionKey]);
				input.setAttribute('data-calc-question-input', '');
				fields.appendChild(input);
			}

			function renderCategories(product, selectedType) {
				var previousCategories = fields.querySelector('[data-calc-categories]');

				if (previousCategories) {
					previousCategories.remove();
				}

				var previousInput = fields.querySelector('[data-calc-question-input]');

				if (previousInput) {
					previousInput.remove();
				}

				var categoryOptions = (typeToCategories[selectedType] || []).filter(function (item) {
					return product[item.categoryKey];
				});

				if (!categoryOptions.length) {
					return;
				}

				var fieldset = createFieldset(
					product.cathegorie1Title || 'Выберите способ внесения:',
					'calc-category',
					categoryOptions.map(function (item) {
						return {
							value: item.questionKey,
							label: product[item.categoryKey]
						};
					}),
					function (questionKey) {
						renderQuestionInput(product, questionKey);
					}
				);
				fieldset.setAttribute('data-calc-categories', '');
				fields.appendChild(fieldset);
				renderQuestionInput(product, categoryOptions[0].questionKey);
			}

			function renderCalculatorFields(product) {
				fields.innerHTML = '';

				if (!product) {
					var message = document.createElement('p');
					message.className = 'yur-calc-field yur-calc-field--wide yur-calc-empty';
					message.textContent = 'Для выбранного препарата пока нет данных для расчета.';
					fields.appendChild(message);
					return;
				}

				var typeOptions = getFilledOptions(product, ['type1', 'type2', 'type3']);

				if (!typeOptions.length) {
					return;
				}

				fields.appendChild(createFieldset(
					product.type1Title || 'Выберите объект:',
					'calc-type',
					typeOptions,
					function (selectedType) {
						renderCategories(product, selectedType);
					}
				));
				renderCategories(product, typeOptions[0].value);
			}

			function normalizeProductTitle(value) {
				return value.replace(/\s*\([^)]*\)\s*/g, '').trim();
			}

			function openCalculator(productName) {
				var product = productsForCalc.find(function (item) {
					return item.title === productName;
				}) || productsForCalc.find(function (item) {
					return normalizeProductTitle(item.title) === productName;
				});

				title.textContent = 'Калькулятор расхода препарата ' + productName;
				renderCalculatorFields(product);
				panel.classList.add('is-active');
				overlay.classList.add('is-active');
				panel.setAttribute('aria-hidden', 'false');
				document.body.style.overflow = 'hidden';
			}

			function closeCalculator() {
				panel.classList.remove('is-active');
				overlay.classList.remove('is-active');
				panel.setAttribute('aria-hidden', 'true');
				document.body.style.overflow = '';
			}

			document.querySelectorAll('.prod-yur-calc-box .btn').forEach(function (button) {
				button.classList.add('yur-calc-open');
				button.setAttribute('type', 'button');
				button.addEventListener('click', function (event) {
					event.preventDefault();
					var card = button.closest('.prod-yur-item');
					var productTitle = card ? card.querySelector('.prod-yur-descr-box h5') : null;
					openCalculator(productTitle ? productTitle.textContent.trim() : '');
				});
			});

			closeElements.forEach(function (element) {
				element.addEventListener('click', closeCalculator);
			});

			document.addEventListener('keydown', function (event) {
				if (event.key === 'Escape') {
					closeCalculator();
				}
			});
		});
	</script>

